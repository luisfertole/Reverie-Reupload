<?php
require_once __DIR__ . '/api/config/auth.php';

header('Content-Type: application/json');

if ($_SERVER['REQUEST_METHOD'] !== 'POST') {
    http_response_code(405);
    echo json_encode(['status' => 'error', 'message' => 'Metodo no permitido']);
    exit;
}

$type = $_POST['type'] ?? '';
$folders = [
    'products' => 'products',
    'employees' => 'employees'
];

if (!isset($folders[$type])) {
    http_response_code(400);
    echo json_encode(['status' => 'error', 'message' => 'Tipo de imagen invalido']);
    exit;
}

if ($type === 'products') {
    auth::requireAdmin(true);
} else {
    auth::requireEmployee(true);
}

if (!isset($_FILES['photo']) || $_FILES['photo']['error'] !== UPLOAD_ERR_OK) {
    http_response_code(400);
    echo json_encode(['status' => 'error', 'message' => 'No se recibio la imagen']);
    exit;
}

$file = $_FILES['photo'];
$maxSize = 4 * 1024 * 1024;

if ($file['size'] > $maxSize) {
    http_response_code(400);
    echo json_encode(['status' => 'error', 'message' => 'La imagen no debe pasar de 4 MB']);
    exit;
}

$finfo = new finfo(FILEINFO_MIME_TYPE);
$mime = $finfo->file($file['tmp_name']);

$extensions = [
    'image/jpeg' => 'jpg',
    'image/png' => 'png',
    'image/webp' => 'webp',
    'image/gif' => 'gif',
    'image/avif' => 'avif'
];

if (!isset($extensions[$mime])) {
    http_response_code(400);
    echo json_encode(['status' => 'error', 'message' => 'Formato de imagen no permitido']);
    exit;
}

$relativeDir = 'assets/photos/' . $folders[$type];
$targetDir = __DIR__ . DIRECTORY_SEPARATOR . str_replace('/', DIRECTORY_SEPARATOR, $relativeDir);

if (!is_dir($targetDir) && !mkdir($targetDir, 0775, true)) {
    http_response_code(500);
    echo json_encode(['status' => 'error', 'message' => 'No se pudo crear la carpeta']);
    exit;
}

$name = $type . '-' . date('YmdHis') . '-' . bin2hex(random_bytes(4)) . '.' . $extensions[$mime];
$targetPath = $targetDir . DIRECTORY_SEPARATOR . $name;

if (!move_uploaded_file($file['tmp_name'], $targetPath)) {
    http_response_code(500);
    echo json_encode(['status' => 'error', 'message' => 'No se pudo guardar la imagen']);
    exit;
}

echo json_encode([
    'status' => 'success',
    'path' => $relativeDir . '/' . $name
]);


