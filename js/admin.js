const todayMoney = document.getElementById('todayMoney');
const todayTickets = document.getElementById('todayTickets');
const activeProducts = document.getElementById('activeProducts');
const employeeCount = document.getElementById('employeeCount');
const lowStockList = document.getElementById('lowStockList');

function setText(idElement, value) {
    idElement.textContent = value;
}

function productState(product) {
    const state = (product.state || '').toLowerCase();

    if (state === 'discontinued') {
        return 'discontinued';
    }

    if (Number(product.amount || 0) <= 0 || state === 'soldout') {
        return 'soldout';
    }

    return 'available';
}

function renderLowStock(products) {
    const low = products
        .filter(product => productState(product) !== 'discontinued')
        .filter(product => Number(product.amount || 0) <= 8)
        .sort((a, b) => Number(a.amount || 0) - Number(b.amount || 0))
        .slice(0, 6);

    if (!low.length) {
        lowStockList.innerHTML = '<div class="empty-state">El inventario esta estable.</div>';
        return;
    }

    lowStockList.innerHTML = low.map(product => {
        const state = productState(product) === 'soldout' ? 'warn' : 'ok';

        return `
            <article class="product-card">
                <img class="product-photo" src="${App.escapeHtml(App.productPhoto(product))}" alt="${App.escapeHtml(product.name || 'Producto')}">
                <div>
                    <h3>${App.escapeHtml(product.name || 'Producto')}</h3>
                    <p>${App.escapeHtml(product.code || '')}</p>
                    <div class="product-meta">
                        <span class="status-pill ${state}">${Number(product.amount || 0)} piezas</span>
                        <span class="status-pill">${App.money(product.price)}</span>
                    </div>
                </div>
            </article>
        `;
    }).join('');
}

async function loadAdminDashboard() {
    const [productsResult, employeesResult, reportResult] = await Promise.allSettled([
        App.request('product'),
        App.request('employee'),
        App.request('dailyreport', { params: { interval: 1 } })
    ]);

    if (productsResult.status === 'fulfilled') {
        const products = App.toArray(productsResult.value);
        const active = products.filter(product => productState(product) !== 'discontinued').length;
        setText(activeProducts, active);
        renderLowStock(products);
    } else {
        lowStockList.innerHTML = '<div class="error-state">No se pudo cargar el inventario.</div>';
    }

    if (employeesResult.status === 'fulfilled') {
        const employees = App.toArray(employeesResult.value);
        setText(employeeCount, employees.length);
    }

    if (reportResult.status === 'fulfilled') {
        const report = App.toArray(reportResult.value)[0];
        setText(todayMoney, App.money(report?.moneyMade || 0));
        setText(todayTickets, report?.totalSales || 0);
    } else {
        setText(todayMoney, App.money(0));
        setText(todayTickets, 0);
    }
}

document.addEventListener('DOMContentLoaded', loadAdminDashboard);

