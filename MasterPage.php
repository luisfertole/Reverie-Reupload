<?php
require_once __DIR__ . '/api/config/auth.php';

function e($value)
{
    return htmlspecialchars((string) $value, ENT_QUOTES, 'UTF-8');
}

function page_start($pageTitle, $pageClass = '')
{
?>
<!DOCTYPE html>
<html lang="es">

<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <meta name="description" content="Sistema de inventarios Reverie para Spiral">
    <title><?php echo e($pageTitle); ?></title>
    <link href="https://cdn.jsdelivr.net/npm/bootstrap@5.3.2/dist/css/bootstrap.min.css" rel="stylesheet">
    <link href="https://cdn.jsdelivr.net/npm/bootstrap-icons@1.11.3/font/bootstrap-icons.css" rel="stylesheet">
    <link rel="stylesheet" href="css/styles.css">
</head>

<body class="app-body <?php echo e($pageClass); ?>">
<?php
}

function page_header($title, $subtitle = '', $backUrl = '', $actions = '', $homeUrl = 'Admin.php')
{
?>
    <header class="app-topbar">
        <div class="topbar-left">
            <?php if ($backUrl !== '') { ?>
                <a class="icon-btn" href="<?php echo e($backUrl); ?>" title="Regresar">
                    <i class="bi bi-arrow-left"></i>
                </a>
            <?php } else { ?>
                <a class="brand-mark" href="<?php echo e($homeUrl); ?>" title="Inicio">
                    <img src="assets/photos/logo_nobg.png" alt="Spiral">
                </a>
            <?php } ?>
            <div>
                <h1><?php echo e($title); ?></h1>
                <?php if ($subtitle !== '') { ?>
                    <p><?php echo e($subtitle); ?></p>
                <?php } ?>
            </div>
        </div>
        <?php if ($actions !== '') { ?>
            <div class="topbar-actions">
                <?php echo $actions; ?>
            </div>
        <?php } ?>
    </header>
<?php
}

function page_footer()
{
?>
    <footer class="app-footer">
        <div>
            <strong>Powered by Reverie</strong>
            <span>Spiral panaderia y reposteria</span>
        </div>
        <span>Copyright 2026</span>
    </footer>
<?php
}

function page_end($scripts = [])
{
    foreach ($scripts as $script) {
        echo '    <script src="' . e($script) . '"></script>' . PHP_EOL;
    }
?>
</body>

</html>
<?php
}
?>


